    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int fila = jTable1.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Por favor seleccion√° una ODT para eliminar.");
        return;
    }

    int confirmacion = JOptionPane.showConfirmDialog(this, "¬øEst√°s seguro que quer√©s eliminar esta ODT?", "Confirmar", JOptionPane.YES_NO_OPTION);

    if (confirmacion == JOptionPane.YES_OPTION) {
        int idOdt = (int) jTable1.getValueAt(fila, 0); // columna 0 = ID
        OdtDao dao = new OdtDao();

        boolean eliminado = dao.eliminarOdt(idOdt);
        if (eliminado) {
            JOptionPane.showMessageDialog(this, "ODT eliminada con √©xito.");
            cargarTablaOdts(); // üîÅ recargar tabla
        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar la ODT.");
        }
    }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int fila = jTable1.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Por favor seleccion√° una ODT para editar.");
        return;
    }

    int idOdt = (int) jTable1.getValueAt(fila, 0);

    OdtDao dao = new OdtDao();
    Odt odt = dao.buscarOdtPorId(idOdt); // Tendr√°s que crear este m√©todo en OdtDao

    if (odt != null) {
        new EditarOdtView(odt).setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "No se pudo cargar la ODT.");
    }
    }//GEN-LAST:event_jButton5ActionPerformed
private void cargarTablaOdts() {
    OdtDao dao = new OdtDao();
    List<Odt> lista = dao.listarOdts();

    DefaultTableModel modelo = new DefaultTableModel();
    modelo.setColumnIdentifiers(new String[] {
        "ID", "Cliente", "Mec√°nico", "Descripci√≥n", "Precio", "Estado", "Ingreso", "Entrega"
    });

    for (Odt o : lista) {
        modelo.addRow(new Object[] {
            o.getId(),
            o.getCliente().getNombre(),
            o.getMecanico().getNombre(),
            o.getDescripcion(),
            o.getCostoTotal(),
            o.getEstado(),
            o.getFechaIngreso(),
            o.getFechaEntrega()
        });
    }

    jTable1.setModel(modelo);
}
